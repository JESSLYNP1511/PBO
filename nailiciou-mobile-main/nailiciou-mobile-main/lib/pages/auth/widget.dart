            _model.textController1!.text, _model.textController4!.text);
        print(response);
        if (response != null) {
          final userId = response['user_id'];
          final name = response['name'];
          final email = response['email'];
          final phoneNumber = response['phone'];

          final SharedPreferences prefs = await SharedPreferences.getInstance();
          await prefs.setInt('userId', userId);
          await prefs.setString('name', name);
          await prefs.setString('email', email);
          await prefs.setString('phoneNumber', phoneNumber);
          ctx.go("/main");

          print(response);
        }
      } else if (_model.mode == "Sign Up") {
        print(_model.textController1);
        final response = await callSignUpApi(
            _model.textController1!.text,
            _model.textController2!.text,
            _model.textController3!.text,
            _model.textController4!.text);
        _model.mode = "Login";
        _model.textController1!.text = "";
        _model.textController2!.text = "";
        _model.textController3!.text = "";
        _model.textController4!.text = "";
        safeSetState(() {});
        print(response);
      }
    } catch (e) {
      // Show error modal
      showDialog(
        context: ctx,
        builder: (BuildContext context) {
          return AlertDialog(
            title: Text('Error'),
            content: Text('An error occurred: $e'),
            actions: <Widget>[
              TextButton(
                child: Text('OK'),
                onPressed: () {
                  Navigator.of(context).pop();
                },
              ),
            ],
          );
        },
      );
    }

    final response = await callSignUpApi(
        "jason@gmail.com", "jason", "08978302298", "password");
    print(response);
